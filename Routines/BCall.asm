; ===============================================================
; LdHLInd
; Loads 2 byte value at (HL)
; Input: HL = address
; Output: HL = value at address
; ===============================================================

LdHLInd:
	ld A, (HL)
	inc HL
	ld H, (HL)
	ld L, A
	ret

; ===============================================================
; Op5ToOp2
; Moves 11 bytes of OP5 to OP2
; ===============================================================

Op5ToOp2:
	ld HL, OP5
	ld DE, OP2
	ld BC, 11
	ldir
	ret